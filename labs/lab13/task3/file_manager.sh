#!/bin/bash
# Указываем, что скрипт должен выполняться в Bash

# Проверяем первый аргумент ($1):
# Если он равен "clean", то удаляем все .tmp файлы.
if [ "$1" = "clean" ]; then
  echo "Удаление временных файлов..."
  rm -f *.tmp           # Удаляем все файлы с расширением .tmp (-f игнорирует ошибки)
  echo "Файлы удалены"

# Если аргумент — число (регулярное выражение ^[0-9]+$), создаем N файлов.
elif [[ "$1" =~ ^[0-9]+$ ]]; then
  echo "Создание $1 временных файлов..."
  
  # Цикл for от 1 до N (значение $1):
  for ((i=1; i<=$1; i++)); do
    touch "$i.tmp"      # Создаем файл с именем i.tmp (например, 1.tmp)
  done
  
  echo "Файлы созданы"

# Если аргумент не распознан (ни "clean", ни число), выводим подсказку.
else
  echo "Использование:"
  echo "  $0 N - создать N файлов"
  echo "  $0 clean - удалить все .tmp файлы"
fi
