#!/bin/bash


if [ $# -eq 0 ]; then
    # Если аргументы не переданы
    length=10  # Устанавливаем длину последовательности по умолчанию
else
    length=$1  # Используем переданное значение длины
fi

result=""  # Инициализируем пустую строку для результата

for ((i=0; i<length; i++)); do
    # Цикл выполняется length раз
    # Генерируем случайное число от 0 до 25 (соответствует A-Z)
    rand=$((RANDOM % 26))  # $RANDOM дает число 0-32767, %26 - остаток от деления на 26
    
    # Преобразуем число в букву (A=65 в ASCII)
    # printf %x преобразует число в шестнадцатеричный формат
    # printf \x преобразует шестнадцатеричное значение в символ
    letter=$(printf "\x$(printf %x $((rand + 65)))")
    
    result+=$letter  # Добавляем букву к результату
done

echo "Случайная последовательность: $result"  # Выводим результат
